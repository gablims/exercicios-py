valor = int(input("Digite um valor inteiro: "))

if valor % 2 == 0:
    print("O valor é divisível por 2" )
else: 
    print("O valor não é divisível por 2")

if valor % 3 == 0:
    print ("O valor é divisível por 3")
else: 
    print("O valor não é divisível por 3")

